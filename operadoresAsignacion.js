var var1 = 10;
var1 += 5
console.log("var1 + 5 =", var1); // 10 + 5 = 15
var1 -= 5;
console.log("var1 - 5 =", var1); // 15 - 5 = 10
var1 *= 2;
console.log("var1 * 2 =", var1); // 10 * 2 = 20
var1 /= 2
console.log("var1 / 2 =", var1); // 20 / 2 = 10
var1 %= 3;
console.log("var1 % 3 =", var1); // 10 % 3 = 1